package com.github.mgeiss.xls2ora.domain;

/**
 *
 * @author Markus Geiss
 * @version 1.0
 */
public class ColumnData {

    private String label;
    private String type;

    public ColumnData() {
        super();
    }

    public ColumnData(String label, String type) {
        super();
        this.label = label;
        this.type = type;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return this.label;
    }
}
